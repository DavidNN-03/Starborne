public enum InputSet /*This enum defines a few different options for different ways to get player-input.*/
{
    mouseX, /*Take input from the horizontal mouse movement.*/
    mouseY, /*Take input from vertical mouse movement.*/
    wAndS, /*Take input from W and S keys.*/
    aAndD, /*Take input from A and D keys.*/
    eAndQ /*Take input from E and Q keys.*/
}
